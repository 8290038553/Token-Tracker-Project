window.addEventListener("load", () => {
    if (window.ethereum) {
        web3 = new Web3(window.ethereum);
        window.ethereum.enable().then(async (accounts) => {
            userAddress = accounts[0];
            contract = new web3.eth.Contract(contractABI, contractAddress);
            updateUI();
        });
    } else {
        alert("Please install MetaMask to use this DApp.");
    }
});

async function updateUI() {
    const balance = await contract.methods.getBalance().call({ from: userAddress });
    const goal = await contract.methods.getFitnessGoal().call({ from: userAddress });

    document.getElementById("wallet-address").textContent = userAddress;
    document.getElementById("balance").textContent = web3.utils.fromWei(balance, "ether") + " FGT";
    document.getElementById("goal").textContent = goal ? goal : "Not set";
}

document.getElementById("set-goal-btn").addEventListener("click", async () => {
    const goal = document.getElementById("goal-input").value;
    if (goal) {
        await contract.methods.setFitnessGoal(goal).send({ from: userAddress });
        updateUI();
    }
});

document.getElementById("track-goal-btn").addEventListener("click", async () => {
    const achieved = prompt("Enter achieved units:");
    if (achieved) {
        await contract.methods.trackGoalAchievement(achieved).send({ from: userAddress });
        updateUI();
    }
});

document.getElementById("transfer-btn").addEventListener("click", async () => {
    const recipient = document.getElementById("recipient").value;
    const amount = document.getElementById("amount").value;
    if (recipient && amount) {
        await contract.methods.transfer(recipient, web3.utils.toWei(amount, "ether")).send({ from: userAddress });
        updateUI();
    }
});
